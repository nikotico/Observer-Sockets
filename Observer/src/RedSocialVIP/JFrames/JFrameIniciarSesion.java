    private void BtnFanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFanActionPerformed
        if (!TxtNombre.getText().isBlank()){
            this.dispose();
            setCliente();
            Fan fan = new Fan(TxtNombre.getText(),c);
            JFrameFan Jfan = new JFrameFan();
            fan.getC().setPantalla(Jfan);
            Jfan.setFan(fan);
            Jfan.setNombre(TxtNombre.getText());
            Jfan.setVisible(true);
            Jfan.setTitle(TxtNombre.getText());
            c.hiloCliente.escribir(ID.GETARTISTA);
        }
        else{
          JOptionPane.showMessageDialog(null, "Ingrese un nombre!!!");
        }
    }//GEN-LAST:event_BtnFanActionPerformed

    private void BtnArtistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnArtistaActionPerformed
        if (!TxtNombre.getText().isBlank()){
            this.dispose();
            setCliente();
            Artista artista = new Artista(TxtNombre.getText(),c);
            JFrameArtista Jartista = new JFrameArtista();
            artista.getC().setPantalla(Jartista);
            Jartista.setArtista(artista);
            Jartista.setNombre(TxtNombre.getText());
            Jartista.setVisible(true);
            Jartista.setTitle(TxtNombre.getText());
            c.hiloCliente.escribir(ID.SETARTISTA);
            c.hiloCliente.escribir(true);
            c.hiloCliente.escribir(artista);
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese un nombre!!!");  
        }
    }//GEN-LAST:event_BtnArtistaActionPerformed

