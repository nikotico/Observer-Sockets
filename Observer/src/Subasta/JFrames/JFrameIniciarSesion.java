    public void appendConsola(String msg){
        consola.append(msg);
        consola.append("\n");
    }
    
    private void accion(){
     //Obtiene el texto enviado
        String txt = txfEscribirConsola.getText();
        //Lo muestra en la consola
        appendConsola("EJECUTANDO  >> " + txt);
        //Separa el string recibido
        String[] parts = txt.split("-");
        switch(parts[0]){
            case "Name":
                c.hiloCliente.escribir(ID.SETNAME);
                c.hiloCliente.escribir(parts[1]);
                 ((JFrameIniciarSesion)c.getRefPantalla()).lblName.setText(parts[1]);
                this.setTitle(parts[1]);//coloca el nombre en el titulo de la pantalla
            break;
            case "Chat":
                c.hiloCliente.escribir(ID.CHAT);
                c.hiloCliente.escribir(parts[1]);
            break;
            default:
                appendConsola("Ups, comando inexistente!");
            break;
        }
        //limpia
        txfEscribirConsola.setText("");
    }
